# Tuples are a fundamental data structure in Python that allow you to store multiple items in a single variable. They are similar to lists, but with some key differences:
# 1. Ordered: Tuples maintain the order of items, meaning that the order in which you add items to a tuple is preserved.
# 2. Immutable: Once a tuple is created, its items cannot be changed, added, or
#     removed. This immutability makes tuples more secure and can lead to performance improvements in certain situations.
# 3. Allow Duplicates: Tuples can contain duplicate values, just like lists.
# 4. Indexed: Items in a tuple can be accessed using their index, with the first item having an index of 0.
# 5. Heterogeneous: Tuples can contain items of different data types, including integers, strings, and even other tuples.
# Tuples are defined by enclosing items in parentheses () and separating them with commas. For example:
# my_tuple = (1, "hello", 3.14, (2, 3))
